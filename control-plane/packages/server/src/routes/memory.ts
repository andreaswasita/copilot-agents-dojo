import { Hono } from "hono";
import { eq, ilike, and } from "drizzle-orm";
import { writeFileSync, existsSync, readFileSync } from "node:fs";
import { join } from "node:path";
import { execSync } from "node:child_process";
import { memoryEntries } from "@dojo/db";
import type { Db } from "@dojo/db";
import {
  scanAllMemory,
  readLinkGraph,
  buildGraphPayload,
  getBacklinksFor,
  getForwardLinksFor,
} from "../memory-scanner.js";

const VALID_TYPES = new Set(["decision", "pattern", "preference", "session"]);
const VALID_TYPE_FOLDERS: Record<string, string> = {
  decision: "decisions",
  pattern: "patterns",
  preference: "preferences",
  session: "sessions",
};

interface ScaffoldInput {
  type: string;
  slugSuffix: string;
  title: string;
  tags?: string[];
  status?: string;
  extraFrontmatter?: Record<string, unknown>;
  initialBody?: string;
}

function validateScaffold(body: unknown): ScaffoldInput | null {
  if (!body || typeof body !== "object") return null;
  const b = body as Record<string, unknown>;
  if (typeof b.type !== "string" || !VALID_TYPES.has(b.type)) return null;
  if (typeof b.slugSuffix !== "string" || !/^[a-z0-9][a-z0-9-]*$/.test(b.slugSuffix)) return null;
  if (typeof b.title !== "string" || b.title.trim().length === 0) return null;
  return {
    type: b.type,
    slugSuffix: b.slugSuffix,
    title: b.title.trim(),
    tags: Array.isArray(b.tags) ? (b.tags as unknown[]).filter((t): t is string => typeof t === "string") : [],
    status: typeof b.status === "string" ? b.status : undefined,
    extraFrontmatter:
      typeof b.extraFrontmatter === "object" && b.extraFrontmatter !== null
        ? (b.extraFrontmatter as Record<string, unknown>)
        : {},
    initialBody: typeof b.initialBody === "string" ? b.initialBody : undefined,
  };
}

function todayIso(): string {
  return new Date().toISOString().slice(0, 10);
}

function buildScaffoldFrontmatter(input: ScaffoldInput): string {
  const fm: Record<string, unknown> = {
    type: input.type,
    date: todayIso(),
    tags: input.tags ?? [],
  };
  if (input.status) fm.status = input.status;
  if (input.type === "decision") {
    fm.status = fm.status || "accepted";
    fm["superseded-by"] = null;
  } else if (input.type === "pattern") {
    fm.source = "control-plane-scaffold";
    fm.error_type = null;
    fm.occurrences = 0;
    fm.status = fm.status || "active";
  } else if (input.type === "preference") {
    fm.source = "control-plane-scaffold";
    fm.confidence = "low";
  } else if (input.type === "session") {
    fm.task = null;
    fm.branch = null;
  }
  Object.assign(fm, input.extraFrontmatter ?? {});

  const lines = ["---"];
  for (const [k, v] of Object.entries(fm)) {
    if (Array.isArray(v)) {
      lines.push(`${k}: [${v.map((x) => JSON.stringify(x)).join(", ")}]`);
    } else if (v === null) {
      lines.push(`${k}: null`);
    } else if (typeof v === "string") {
      lines.push(`${k}: ${v}`);
    } else {
      lines.push(`${k}: ${JSON.stringify(v)}`);
    }
  }
  lines.push("---");
  return lines.join("\n");
}

