async function rescanMemory(db: Db, dojoRoot: string): Promise<number> {
  const scanned = await scanAllMemory(dojoRoot);
  for (const entry of scanned) {
    const existing = await db
      .select()
      .from(memoryEntries)
      .where(eq(memoryEntries.slug, entry.slug))
      .limit(1);
    if (existing.length > 0) {
      await db
        .update(memoryEntries)
        .set({ ...entry, updatedAt: new Date() })
        .where(eq(memoryEntries.slug, entry.slug));
    } else {
      await db.insert(memoryEntries).values(entry);
    }
  }
  return scanned.length;
}

export function memoryRoutes(db: Db, dojoRoot: string) {
  const router = new Hono();

  router.get("/", async (c) => {
    const type = c.req.query("type");
    const search = c.req.query("search");
    const tag = c.req.query("tag");

    const conditions = [];
    if (type) conditions.push(eq(memoryEntries.type, type));
    if (search) conditions.push(ilike(memoryEntries.title, `%${search}%`));

    let query = db.select().from(memoryEntries);
    if (conditions.length > 0) {
      query = query.where(and(...conditions)) as typeof query;
    }
    let results = await query.orderBy(memoryEntries.type, memoryEntries.title);
    if (tag) {
      results = results.filter((r) => r.tags.includes(tag));
    }
    return c.json(results);
  });

  router.get("/graph", async (c) => {
    const all = await db
      .select({
        slug: memoryEntries.slug,
        type: memoryEntries.type,
        title: memoryEntries.title,
      })
      .from(memoryEntries);
    const linkGraph = readLinkGraph(dojoRoot);
    return c.json(buildGraphPayload(all, linkGraph));
  });

  router.get("/tags", async (c) => {
    const all = await db.select({ tags: memoryEntries.tags }).from(memoryEntries);
    const tagSet = new Set<string>();
    for (const row of all) for (const t of row.tags) tagSet.add(t);
    return c.json([...tagSet].sort());
  });

  router.post("/scan", async (c) => {
    const count = await rescanMemory(db, dojoRoot);
    return c.json({ message: "Memory scan complete", count });
  });

  router.post("/scaffold", async (c) => {
    const parsed = validateScaffold(await c.req.json());
    if (!parsed) {
      return c.json(
        {
          error:
            "Invalid scaffold data. Required: type (decision/pattern/preference/session), slugSuffix (lowercase-hyphen), title (non-empty)",
        },
        400
      );
    }

    const folder = VALID_TYPE_FOLDERS[parsed.type];
    const memoryDir = join(dojoRoot, "memory");
    if (!existsSync(memoryDir)) {
      return c.json({ error: "Memory vault not found at " + memoryDir }, 500);
    }

    const filename =
      parsed.type === "decision" || parsed.type === "session"
        ? `${todayIso()}-${parsed.slugSuffix}.md`
        : `${parsed.slugSuffix}.md`;
    const targetDir = join(memoryDir, folder);
    const targetPath = join(targetDir, filename);

    if (existsSync(targetPath)) {
      return c.json({ error: `File already exists: ${folder}/${filename}` }, 409);
    }

    const frontmatter = buildScaffoldFrontmatter(parsed);
    const body = parsed.initialBody?.trim() || defaultBody(parsed);
    const fileContent = `${frontmatter}\n\n${body.trim()}\n`;

    writeFileSync(targetPath, fileContent, "utf-8");

    let linkIndexRan = false;
    const linkScript = join(dojoRoot, "scripts", "link-index.sh");
    if (existsSync(linkScript)) {
      try {
        execSync(`bash ${linkScript}`, { cwd: dojoRoot, stdio: "ignore" });
        linkIndexRan = true;
      } catch {
        // continue — graph just won't have new node until next manual run
      }
    }

    await rescanMemory(db, dojoRoot);

    const slug = `${folder}/${filename.replace(/\.md$/, "")}`;
    return c.json(
      {
        slug,
        path: targetPath,
        linkIndexRan,
      },
      201
    );
  });

  router.get("/:slug{.+}", async (c) => {
    const slug = c.req.param("slug");
    const result = await db
      .select()
      .from(memoryEntries)
      .where(eq(memoryEntries.slug, slug))
      .limit(1);
    if (result.length === 0) return c.json({ error: "Memory entry not found" }, 404);

    const linkGraph = readLinkGraph(dojoRoot);
    const entry = result[0];
    let renderedMarkdown = entry.markdown;
    try {
      renderedMarkdown = readFileSync(entry.filePath, "utf-8").replace(/^---[\s\S]*?---\n?/, "").trim();
    } catch {
      // fall back to DB markdown
    }

    return c.json({
      ...entry,
      markdown: renderedMarkdown,
      backlinks: getBacklinksFor(slug, linkGraph),
      forwardLinks: getForwardLinksFor(slug, linkGraph),
    });
  });

  return router;
}
